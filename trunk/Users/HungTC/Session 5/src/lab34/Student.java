package lab34;



public class Student {
private int age;
private double mark;
private String name;
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}
public double getMark() {
	return mark;
}
public void setMark(double mark) {
	this.mark = mark;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}




}
